
# Conversão sw Tipos em Python

# Convertendo Int para float

preco = 10 
preco = float(preco)
print(preco)

# Convertendo float para Int

preco = 10.0
preco = int(preco)
print(preco)


# Conversão por divisão, barra unica convert para float

preco = 10 
print(preco/2)


# Conversão por divisão, barra dupla mantem em int

preco = 10 
print(preco//2)

# Numérico para String

idade = 28
print(str(idade))

# String para Numérico

preco2 = "10.50"
print(float(preco2))
